    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

        String idCliente = txtID.getText();

        PreparedStatement ps = null;
        Conexion conn = new Conexion();
        Connection con = (Connection) conn.getConnection();

        try{

            String sql = "select * from clientes where idCliente = '"+idCliente+"';";

            ps = con.prepareCall(sql);
            ResultSet res = ps.executeQuery();

            if(res.next()){
                txtID.setText(res.getString("idCliente"));
                txtNombre.setText(res.getString("nombre"));
                txtApellido.setText(res.getString("apellido"));
                txtDocumento.setText(res.getString("documento"));
                txtTelefono.setText(res.getString("telefono"));
                txtFechaRegistro.setText(res.getString("fechaRegistro"));
            }

        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }

    }//GEN-LAST:event_btnBuscarActionPerformed

    private void btnBorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBorrarActionPerformed

        String idCliente = txtID.getText();

        PreparedStatement ps = null;
        Conexion conn = new Conexion();
        Connection con = (Connection) conn.getConnection();

        int respuesta = JOptionPane.showConfirmDialog(null, "Borrar registro.");

        if(respuesta == JOptionPane.YES_OPTION)
        {
            try{

                String sql = "DELETE FROM `sgi`.`clientes` WHERE (`idCliente` = '"+txtID.getText()+"');";

                ps = con.prepareCall(sql);
                int res = ps.executeUpdate();

                JOptionPane.showMessageDialog(null, "Registro borrado!");

            }catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }

    }//GEN-LAST:event_btnBorrarActionPerformed

