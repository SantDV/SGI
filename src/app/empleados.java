    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed

            String idEmpleado = txtID.getText();
            String nombre = txtNombre.getText();
            String apellido = txtApellido.getText();
            String documento = txtDocumento.getText();
            String telefono = txtTelefono.getText();
            String sql = "";
        

        PreparedStatement ps = null;
        Conexion conn = new Conexion();
        Connection con = (Connection) conn.getConnection();

        try{

            if(!"".equals(idEmpleado))
                {
                      sql = "select idEmpleado, empleados.nombre, apellido, documento, telefono, domicilio, sectores.nombre, fechaRegistro, usuarios.usuario from empleados, sectores, usuarios where idEmpleado = '"+idEmpleado+"' and empleados.sector = sectores.idSector and empleados.usuario = usuarios.idUsuario;";
                    
                }
                else if (!"".equals(nombre)){
                    
                      sql = "select idEmpleado, empleados.nombre, apellido, documento, telefono, domicilio, sectores.nombre, fechaRegistro, usuarios.usuario from empleados, sectores, usuarios where empleados.nombre = '"+nombre+"' and empleados.sector = sectores.idSector and empleados.usuario = usuarios.idUsuario;";
                }
                else if (!"".equals(apellido)){
                    
                      sql = "select idEmpleado, empleados.nombre, apellido, documento, telefono, domicilio, sectores.nombre, fechaRegistro, usuarios.usuario from empleados, sectores, usuarios where empleados.apellido = '"+apellido+"' and empleados.sector = sectores.idSector and empleados.usuario = usuarios.idUsuario;";
                }
                else if (!"".equals(documento)){
                    
                      sql = "select idEmpleado, empleados.nombre, apellido, documento, telefono, domicilio, sectores.nombre, fechaRegistro, usuarios.usuario from empleados, sectores, usuarios where documento = '"+documento+"' and empleados.sector = sectores.idSector and empleados.usuario = usuarios.idUsuario;";
                }
                else if (!"".equals(telefono)){
                    
                      sql = "select idEmpleado, empleados.nombre, apellido, documento, telefono, domicilio, sectores.nombre, fechaRegistro, usuarios.usuario from empleados, sectores, usuarios where telefono = '"+telefono+"' and empleados.sector = sectores.idSector and empleados.usuario = usuarios.idUsuario;";
                }
            

            ps = con.prepareCall(sql);
            ResultSet res = ps.executeQuery();

            if(res.next()){
            
                txtID.setText(res.getString("idEmpleado"));
                txtNombre.setText(res.getString("nombre"));
                txtApellido.setText(res.getString("apellido"));
                txtDocumento.setText(res.getString("documento"));
                txtTelefono.setText(res.getString("telefono"));
                txtDomicilio.setText(res.getString("domicilio"));     
                txtFechaRegistro.setText(res.getString("fechaRegistro"));
                txtUsuario.setText(res.getString("usuarios.usuario")); 
            }

        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

